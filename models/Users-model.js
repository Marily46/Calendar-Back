const { Schema, model } = require('mongoose');

const UsersSchema = Schema({
    name: {
        type: String,
        require: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
});

module.exports = model('Users-model', UsersSchema);